# Assignment statement
x = 5       # variable x stores the value 5.
print(x)    # retrieve and display the value of variable x.

# Expression and reassignment
x = x + 5   # takes the current value of x, adds 5 in it, store back its final value in x.
print(x)    # print the final value of x **10**. 

#operators
y =  -4
sum_value = x + y  # add value of x and y, store the result in sum_value
print(sum_value)   # display the calculated sum from variable.

# print() is a built-in function
print("Learning Python fundamentals")  

